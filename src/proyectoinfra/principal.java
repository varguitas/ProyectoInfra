    private void InputConsolaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_InputConsolaKeyPressed
        if (evt.getKeyCode() == 10) {
            ColaComandos.add(InputConsola.getText());
            Bitacora.setText(Bitacora.getText()+"\n> ListaComandos: Add --> "+InputConsola.getText());
            InputConsola.setText("");
        }
    }//GEN-LAST:event_InputConsolaKeyPressed

    private void btn_iniciarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_iniciarActionPerformed
        int total_comandos = ColaComandos.size();
        int indice = 0;
        String comando_actual;
        while(total_comandos > indice){
            btn_iniciar.setText("Ejecuntando");
            comando_actual = ColaComandos.remove(0);
            System.out.println("-."+comando_actual.toLowerCase().trim()+".-");
            if (comando_actual.toLowerCase().trim().equals("pause();") || comando_actual.toLowerCase().trim().equals("pause().") || comando_actual.toLowerCase().trim().equals("pause() ")) {
                Bitacora.setText(Bitacora.getText()+"\n> "+comando_actual);
                Bitacora.setText(Bitacora.getText()+"\n> (Paused).");
                break;
            }
            if (consola.parse_query(comando_actual)) {
                Bitacora.setText(Bitacora.getText()+"\n> "+comando_actual);
                Bitacora.setText(Bitacora.getText()+"\n> (Done).");
            } else {
                Bitacora.setText(Bitacora.getText()+"\n> Command Failed!");
            }
            indice++;
        }
        btn_iniciar.setText("Iniciar");
    }//GEN-LAST:event_btn_iniciarActionPerformed

